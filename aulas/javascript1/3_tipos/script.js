// tipos do JS:

// string(texto), number(número), booleano(true- verdadeiro ou false - falso),
//object(objeto), arrays(listas), undefined(indefinido), functions(funções), null(nulo)

//string
let nome = "Rafaela";
console.log(nome, typeof nome);

//number
let idade = 17;
console.log(idade, typeof idade);

//booleano
const maravilhosa = true;
console.log(maravilhosa, typeof maravilhosa);

//undefined - indefinido
let mosquito;
console.log(mosquito, typeof mosquito);

//arrays - listas
const comprarNaFeira = ["pastel", "caldo de cana", "manga", "castanha", "amendoim"];
console.log(comprarNaFeira, typeof comprarNaFeira);

//object
const alunas = {
    fernanda:["milionária", "olho verde", "perfeita"],
    natasha:"milionária",
    rafaela: true,
}
console.log(alunas, typeof alunas);

//valor null(nulo)
const valorDoBozo = null;
console.log(valorDoBozo, typeof valorDoBozo);